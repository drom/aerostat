function getFileName (i) {
    return ('00' + i).slice(-3) + '.mp3';
}

function getUrl (i) {
    if (i >= 728) {
        if (vgtrk[i] === undefined) {
            return;
        }
        return 'https://audio.vgtrk.com/listen?id=' + vgtrk[i][0];
    }
    if (i >= 600) {
        return 'https://aerostatica.ru/music/' + ('00' + i).slice(-3) + '.mp3';
    }
    return 'http://aquarium.lipetsk.ru/MESTA/mp3/Aerostat/'
        + 'Aerostat_vol_' + i
        + '/Boris%20'
        + ((i < 284) ? 'Grebentshikov' : 'Grebenshchikov')
        + '%20-%20Aerostat%20Radio%20vol.' + i + '.mp3';
}

